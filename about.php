<center><h1>ABOUT US</h1></center>
<p>We sell Ebook direct to consumers via SMASHWORDS.com
We provide a range of Smashwords-specific technical services to book publishers
Launched in 2000, smashwords.com is a popular ebook retailer with a reputation for innovation, integrity and independence. We sell ebooks direct to millions of consumers around the world, with five local sales portals in the US, Canada, UK, Europe and Australia.</p>

<p>You can read our ebooks online, or download them to a vast array of devices, using our Smashwords Reader apps.</p>

<p>smashwords.com is privately held and run by its founders, life-long booksellers Stephen and Trudy Cole. It is the only independent ebook retailer that sells into every country in the world, and the International Space Station.</p>

<p>We have our own publisher licenses, delivery platforms, reader apps and repository of titles. Ebook Reader, our mobile app, is one of the most popular apps in the ebook market, with millions of installs.</p>

<p>Smashwords.com hosts over a million unique ebook titles and has millions of members.</p>

<p>In addition to our popular ebook-store, we provide services to book publishers. Digital Comps is a system that enables publishers to send secure ebooks to reviewers and instructors, called “complimentary copies”. Ebook Engine is a system that enables publishers to sell ebooks direct to consumers from their own website. And publishers use our Ebook Collections platform to sell collections of their ebooks direct to institutions.</p>

<p><b>Mobile Apps</b><br>

Our proprietary mobile apps are subject to intense development, particularly the Android version. 300,000 people are using our iOS and Android apps every week. The apps are developed in-house, from end to end. They are not white label versions of someone else’s apps.</p>

<p><b>Proprietary DRM</b><br>

We have our own DRM system that has secured the content delivered via our Ebook Reader apps since 2007. It is not based on Adobe ACS. Having our own DRM system improves usability for our customers, and enables us to respond quickly to new requirements and trends.</p>

<p><b>Online (Browser-Based) Reader App</b></p>

<p>Our proprietary web-based reader enables desktop users to immediately start reading, without having to download or install software. Currently undergoing an upgrade to make it suitable for mobile platforms. Suitable for retail and library use.</p>

<p><b>Email Newsletter Program</b></p>

<p>We create 350 unique, subject-based new title alerts every month for 200,000 opt-in subscribers.</p>

<p><b>Four Thousand Publishers</b><br>

<p>We maintain license agreements covering the output of over 4,000 publishers. Our publisher partners include all the major English language trade, professional and scholarly presses.</p>

<p><b>Millions of Unique, High Quality Titles</b><br>

<p>The company hosts 1.2 million professionally published ebooks, including popular trade titles, and professional, educational and scholarly texts. We hold them in the form of PDFs and EPUB files.</p>

<p><b>Publishers’ Management Portal</b><br>

<p>“Pi” is our highly regarded, dedicated online portal where publishers can manage their preferences, fetch reports and so on.</p>

<p><b>Ebook Processing Platform</b><br>

Publishers upload 25,000 ebooks to us every month. Our production unit has been enhanced and modified steadily since March 2000, and has evolved along with the industry. It deals elegantly with all the complexities thrown up by an evolving sector, including faulty files and metadata supplied by publishers.</p>

<p><b>The Team</b><br>

Deep, broad, detailed domain expertise. The company comprises a small, team of long-time ebook specialists. Our newest employee was engaged in 2010. Many others have been with us for over 10 years; some go back to 2001. It’s fair to say that our team knows pretty much everything about processing, managing and securely delivering ebooks to a global audience.</p>
